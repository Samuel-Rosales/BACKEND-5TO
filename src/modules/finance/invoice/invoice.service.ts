    async create(data: CreateInvoiceDto) {
        try {
            const { created, commissions } = await prisma.$transaction(async (tx) => {
                const exchangeRate = await resolveExchangeRate(data.exchangeRateId, tx);
                const status = await this.resolveStatusId(data.statusId, tx);

                const taxId = data.taxId ?? (await this.resolveDefaultTaxId(tx));

                if (data.payments.length === 0) {
                    throw new Error("Debe haber al menos un pago");
                }

                let totalPaymentUsd = 0;
                for (const payment of data.payments) {
                    const method = await tx.paymentMethod.findUnique({ where: { id: payment.paymentMethodId } });
                    if (!method) throw new Error(`El método de pago con ID ${payment.paymentMethodId} no existe`);
                    
                    if (method.currency !== "USD") {
                        totalPaymentUsd += Number(payment.amount_paid) * Number(exchangeRate.rate);
                    } // esto es temporal, luego se permitirá en otras monedas
                    totalPaymentUsd += payment.amount_paid;
                }

                let totalUsd: number | null = null;
                let computedCommissions: ReturnType<typeof computeCommissions> | null = null;

                if (data.appointmentId) {
                    const appointment = await tx.appointment.findUnique({
                        where: { id: data.appointmentId },
                        select: { doctorId: true, patientId: true },
                    });
                    if (!appointment) throw new Error("La cita no existe");
                    if (appointment.patientId !== data.patientId) throw new Error("La cita no pertenece al paciente");

                    const doctor = await tx.doctor.findUnique({
                        where: { id: appointment.doctorId },
                        select: { specialtyId: true },
                    });
                    if (!doctor) throw new Error("El doctor de la cita no existe");

                    const specialty = await tx.medicalSpecialty.findUnique({
                        where: { id: doctor.specialtyId },
                        select: { consultation_price: true, commission_percentage: true },
                    });
                    if (!specialty) throw new Error("La especialidad del doctor no existe");

                    totalUsd = Number(specialty.consultation_price);

                    const clinicPercent = Number(specialty.commission_percentage);
                    if (!Number.isNaN(clinicPercent)) {
                        computedCommissions = computeCommissions(
                            [{ quantity: 1, unit_price: totalUsd, is_commissionable: true }],
                            clinicPercent,
                        );
                    }
                } else {
                    if (data.total_usd === undefined || data.total_usd === null) {
                        throw new Error("Debe indicar total_usd o appointmentId");
                    }
                    totalUsd = Number(data.total_usd);
                }

                if (totalUsd === null || Number.isNaN(totalUsd)) {
                    throw new Error("total_usd inválido");
                }

                if (totalPaymentUsd < totalUsd) {
                    return {
                        status: 400,
                        message: `El total pagado en USD (${totalPaymentUsd.toFixed(2)}) es menor al total de la factura (${totalUsd.toFixed(2)})`,
                        data: null,
                    };  
                }

                const created = await tx.invoice.create({
                    data: {
                        patientId: data.patientId,
                        receptionistId: data.receptionistId,
                        exchangeRateId: exchangeRate.id,
                        statusId: status.id,
                        taxId,

                        total_usd: totalUsd,
                    },
                    select: invoiceSelect,
                });

                for (const payment of data.payments) {
                    await tx.invoicePayment.create({
                        data: {
                            invoiceId: created.id,
                            paymentMethodId: payment.paymentMethodId,
                            amount_paid: payment.amount_paid,
                            igtf_amount: payment.igtf_amount,
                            exchangeRateId: exchangeRate.id,
                        },
                    });
                }

                if (data.appointmentId) {
                    const appointment = await tx.appointment.findUnique({
                        where: { id: data.appointmentId },
                        select: { doctorId: true, date_time: true },
                    });

                    if (!appointment) {
                        throw new Error("La cita no existe");
                    }

                    await tx.consultation.create({
                        data: {
                            invoiceId: created.id,
                            doctorId: appointment.doctorId,
                            date: appointment.date_time,
                            status: ConsultationStatus.PENDING,
                        },
                    });
                }

                return { created, commissions: computedCommissions };
            });

            return {
                status: 201,
                message: "Factura creada éxitosamente",
                data: { ...created, commissions },
            };
        } catch (error) {
            console.error("Error creando la factura:", error);

            return {
                status: 500,
                message: "Error interno al crear la factura",
                error: error instanceof Error ? error.message : "Error desconocido",
            };
        }
    }

    async findAll() {
        try {
            const invoices = await this.db.invoice.findMany({
                orderBy: { id: "desc" },
                select: invoiceSelect,
            });

            const invoicesProcessed = invoices.map((invoice) => {
                const exchangeRate = invoice.exchangeRate.rate;
                
                let totalBs = new Decimal(Number(invoice.total_usd));
                 
                totalBs = totalBs.mul(new Decimal(exchangeRate));


                return { ...invoice, total_bs: totalBs.toNumber() };
                
            });

            return {
                status: 200,
                message: invoices.length === 0 ? "No se encontraron facturas" : "Facturas encontradas éxitosamente",
                data: invoicesProcessed,
            };
        } catch (error) {
            console.error("Error buscando facturas:", error);

            return {
                status: 500,
                message: "Error interno al buscar facturas",
                error: error instanceof Error ? error.message : "Error desconocido",
            };
        }
    }

