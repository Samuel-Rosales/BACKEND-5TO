    constructor(private db: DbClient = prisma) { }

    private async resolveStatusId(statusId?: number, tx?: Prisma.TransactionClient) {
        if (statusId) {
            const status = await (tx || this.db).statusInvoice.findUnique({ where: { id: statusId } });
            if (!status) throw new Error("El status de la factura no existe");
            return status;
        }

        const proforma = await (tx || this.db).statusInvoice.findFirst({ where: { name: { equals: "Proforma", mode: "insensitive" } } });
        if (proforma) return proforma;

        const any = await (tx || this.db).statusInvoice.findFirst({ orderBy: { id: "asc" } });
        if (!any) throw new Error("No existe ningún status de factura (cree uno primero)");
        return any;
    }

    private async resolveDefaultTaxId(tx?: Prisma.TransactionClient) {
        const tax = await (tx || this.db).tax.findFirst({ where: { isActive: true }, orderBy: { id: "asc" } });
        if (!tax) throw new Error("No existe ningún impuesto activo (cree al menos uno)");
        return tax.id;
    }

    private async autoGenerateDetails(consultationId: number): Promise<CreateInvoiceDetailDto[]> {
        const consultation = await this.db.consultation.findUnique({
            where: { id: consultationId },
            select: {
                id: true,
                doctor: {
                    select: {
                        specialty: {
                            select: {
                                name: true,
                                consultation_price: true,
                            },
                        },
                    },
                },
                supplies: {
                    select: {
                        quantity: true,
                        supply: {
                            select: {
                                id: true,
                                name: true,
                                cost_price: true,
                            },
                        },
                    },
                },
            },
        });

        if (!consultation) throw new Error("La consulta no existe");

        const taxId = await this.resolveDefaultTaxId();

        const details: CreateInvoiceDetailDto[] = [];

        details.push({
            description: `Consulta - ${consultation.doctor.specialty.name}`,
            quantity: 1,
            unit_price: Number(consultation.doctor.specialty.consultation_price),
            taxId,
            is_commissionable: true,
        });

        for (const supply of consultation.supplies) {
            const supplyQty = Number(supply.quantity);
            const unitPrice = Number(supply.supply.cost_price) * supplyQty;

            details.push({
                productId: supply.supply.id,
                description: `Insumo - ${supply.supply.name} (x${supplyQty})`,
                quantity: 1,
                unit_price: unitPrice,
                taxId,
                is_commissionable: false,
            });
        }

        return details;
    }

